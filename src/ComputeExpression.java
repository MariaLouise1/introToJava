public class ComputeExpression {
    public static void main(String[] args) {
        System.out.print("(10.5 + 2 * 3) / (45 - 3.5) = ");
        System.out.println((10.5 + 2 * 3) / (45 - 3.5));

        System.out.print("(7.5 * 6.5) - (4.5 * 3) / (47.5 * 5.5) = ");
        System.out.print((7.5 * 6.5) - (4.5 * 3) / (47.5 * 5.5));
    }
}
